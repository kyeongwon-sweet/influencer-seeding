// 특정하기 어려움 / 전 제품 혼합 — 수동 지정용 특수 코드.
export const PRODUCT_CODE_NONE = "없음";
export const PRODUCT_CODE_MIXED = "혼합";

const norm = (s: string) => (s || "").replace(/\s+/g, "").toLowerCase();
const BY_NORM = new Map(Object.entries(PRODUCT_CODES).map(([name, code]) => [norm(name), code]));
// 특수값도 이름 자체로 들어오면 그대로 코드 반환
BY_NORM.set(norm(PRODUCT_CODE_NONE), PRODUCT_CODE_NONE);
BY_NORM.set(norm(PRODUCT_CODE_MIXED), PRODUCT_CODE_MIXED);

/**
 * 상품명 → 제품코드. 공백·대소문자 무시 정확 일치. 매핑에 없으면 null(호출측에서 '-' 또는 '없음' 처리).
 */
export function productCodeOf(name: string | null | undefined): string | null {
  if (!name) return null;
  return BY_NORM.get(norm(name)) ?? null;
}
